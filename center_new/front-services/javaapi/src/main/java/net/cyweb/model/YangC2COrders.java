package net.cyweb.model;

import javax.persistence.Column;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;
import java.math.BigDecimal;

@Table(name="yang_c2c_orders")
public class YangC2COrders extends BaseEntity{
    @Id
    @Column(name = "id")
    @GeneratedValue(generator = "JDBC")//此处加上注解
    private Integer id;
    @Column(name = "trade_no")
    private String tradeNo;
    @Column(name = "gua_id")
    private Integer guaId;
    @Column(name = "currency_id")
    private Integer currencyId;
    @Column(name = "price")
    private BigDecimal price;
    @Column(name = "buy_money")
    private String buyMoney;
    @Column(name = "buy_status")
    private Integer buyStatus;
    @Column(name = "add_time")
    private String addTime;
    @Column(name = "invalid_time")
    private String invalidTime;
    @Column(name = "type")
    private String type;
    @Column(name = "num")
    private BigDecimal num;
    @Column(name = "member_id")
    private Integer memberId;
    @Column(name = "card_detail")
    private String cardDetail;
    @Column(name = "nick_name")
    private String nickName;
    @Column(name = "bank_id")
    private Integer bankId;
    @Column(name = "cankao_num")
    private String cankaoNum;
    @Column(name="order_num")
    private BigDecimal orderNum;

    public BigDecimal getOrderNum() { return orderNum; }

    public void setOrderNum(BigDecimal orderNum) { this.orderNum = orderNum; }
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTradeNo() {
        return tradeNo;
    }

    public void setTradeNo(String tradeNo) {
        this.tradeNo = tradeNo;
    }

    public Integer getGuaId() {
        return guaId;
    }

    public void setGuaId(Integer guaId) {
        this.guaId = guaId;
    }

    public Integer getCurrencyId() {
        return currencyId;
    }

    public void setCurrencyId(Integer currencyId) {
        this.currencyId = currencyId;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public String getBuyMoney() {
        return buyMoney;
    }

    public void setBuyMoney(String buyMoney) {
        this.buyMoney = buyMoney;
    }

    public Integer getBuyStatus() {
        return buyStatus;
    }

    public void setBuyStatus(Integer buyStatus) {
        this.buyStatus = buyStatus;
    }

    public String getAddTime() {
        return addTime;
    }

    public void setAddTime(String addTime) {
        this.addTime = addTime;
    }

    public String getInvalidTime() {
        return invalidTime;
    }

    public void setInvalidTime(String invalidTime) {
        this.invalidTime = invalidTime;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public BigDecimal getNum() {
        return num;
    }

    public void setNum(BigDecimal num) {
        this.num = num;
    }

    public Integer getMemberId() {
        return memberId;
    }

    public void setMemberId(Integer memberId) {
        this.memberId = memberId;
    }

    public String getCardDetail() {
        return cardDetail;
    }

    public void setCardDetail(String cardDetail) {
        this.cardDetail = cardDetail;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public Integer getBankId() {
        return bankId;
    }

    public void setBankId(Integer bankId) {
        this.bankId = bankId;
    }

    public String getCankaoNum() {
        return cankaoNum;
    }

    public void setCankaoNum(String cankaoNum) {
        this.cankaoNum = cankaoNum;
    }
}
